package ejercicios;



public class Ejercicio_4 {

    public static void main(String[] args) {
        char[][] matriz = new char[5][5];

        // Inicializar la matriz
        for (int i = 0; i < matriz.length; i++) {
            for (int j = 0; j < matriz[i].length; j++) {
                if (i == j) {
                    matriz[i][j] = '0';
                } else {
                    matriz[i][j] = '*';
                }
            }
        }

        // Imprimir la matriz
        for (int i = 0; i < matriz.length; i++) {
            for (int j = 0; j < matriz[i].length; j++) {
                System.out.print(matriz[i][j] + " ");
            }
            System.out.println();
        }
    }
}
